#include "main.h"
/**
 *  reset_to_98 - Resets the value of an integer pointer to 98.
 *
 * This function takes a pointer to an integer as its argument and sets the
 * value of the integer to 98. It effectively changes the value pointed to by
 * the pointer to 98.
 *
 * @n: A pointer to an integer variable.
 */
void reset_to_98(int *n)
{
*n = 98;
}
